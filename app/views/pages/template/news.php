virb.$(function()
{
    
	var newsPagination = new Pagination({
		perPage: 5, 
		totalResults: 12,
		model: 'build/News',
		ajaxCall: 'changePage',
		container: '#newsPagination',
		params: {
		    filter: ''
		}
    });

    // attach change event to selectbox for filtering
    $("#news-filter").live('change', function(e) {
    	
    	// get the option value
    	var filterValue = $(this).find('option:selected').val().toString();

    	// set the new filter value
    	newsPagination.params.filter = filterValue;

    	// update!
    	newsPagination.update();

    	if( filterValue == '' ) { filterValue = "All"; }

    	// update select box
    	$(".current-selection").html(filterValue);

    	// supress "are you sure you want to leave" warning
		window.onbeforeunload = null;

    });
});
