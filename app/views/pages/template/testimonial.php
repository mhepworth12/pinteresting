		<div class="interview-bio">
			<h2>EAYRSLEE</h2>
			<p>Business / New York, NY</p>
		</div>
		<img class="interview-images" alt="" src="http://g.virbcdn.com/_assets/_graphics/_showcase/eayrslee-header.jpg">
		<h2>Mimi Eayrs and Joanne Lee are two fashion-savvy New Yorkers who came together to create EAYRSLEE accessories in 2010 and haven’t looked back since.</h2>
		<p>These two former coworkers combined their mutual love for design and fashion to create a line of accessories that are both functional and beautiful. We were so glad to be able to grab a few minutes of time with these busy ladies to learn how they make it all happen.</p>
		<p><a href="http://www.eayrslee.com">eayrslee.com →</a></p>
	</div>
	<div class="interview-section split-left">
		<h3>Where are both of you from?</h3>
		<div class="image-container">
			<img class="image" alt="" src="http://g.virbcdn.com/_assets/_graphics/_showcase/eayrslee-joanne.jpg">
			<h2>Joanne</h2>
			<p>I was born in Seoul and raised in California. I was always making stuff, drawing, and sewing as a kid and teenager but ultimately took a more traditional route of education at UC Berkeley. Two years after graduating and not having had any jobs I enjoyed, I found myself waitressing and finally decided to pursue fashion.</p>
		</div>
	</div>
	<div class="interview-section split-right mimi">
		<div class="image-container">
			<img class="image" alt="" src="http://g.virbcdn.com/_assets/_graphics/_showcase/eayrslee-mimi.jpg">
			<h2>Mimi</h2>
			<p>I was born in Buenos Aires from an Argentine mother and an English father. My mom was studying Fine Arts when I was 5 years old so I was always surrounded by it. After high school I moved to Florence to study art and then finally to New York for a Fine Arts degree at Parsons. Surrounded by fashion students, I slowly made my way into the industry, first as a graphic designer and later as an art director.</p>
		</div>
	</div>
	<div class="interview-section">
		<img alt="" src="http://g.virbcdn.com/_assets/_graphics/_showcase/eayrslee-modelbags.jpg">
	</div>
	<div class="interview-section split-left">
		<img alt="" src="http://g.virbcdn.com/_assets/_graphics/_showcase/eayrslee-open.jpg">
	</div>
	<div class="interview-section split-right margin">
		<h3>What’s the story behind EAYRSLEE?</h3>
		<p>We met at the Brooklyn handbag brand Hayden Harnett where Mimi was the art director and Joanne was the head designer of handbags.</p>
		<blockquote>We always had thought, "What if we had our own company and could do anything we wanted?" </blockquote>
		<p>Then one day in Spring 2010, we were flipping through some magazines and came across an image of Rosita Missoni (the matriarch of the Missoni house), and it was everything that we imagined our company would be. We took it as a sign and figured that it was now</br >or never.</p>
		<p>We've been friends for 5 years and partners for 2.5. We already knew that we worked well together from before so it wasn't a hard transition to start a company together. It's definitely been a learning process so far, and we always joke about the company being our baby. It's true in a way though—everything we do, we consider what would be best for the company baby, and we're raising it in hopes that one day it will be successful.</p>
	</div>
	<div class="interview-section split-left">
		<h3>How would you describe EAYRSLEE?</h3>
		<p>EAYRSLEE represents the duality of the two of us. We are very different from each other but kind of the same, which makes for an interesting dynamic that is reflected in our designs. I think customers respond to the clean lines of the bags (Joanne's influence) but maybe in a crazy color or with some unexpected pop (Mimi’s).</p>
		<h3>What inspires you?</h3>
		<p>We love the idea of rummaging through your grandmother's closet
		and finding old things that seem somehow still relevant or even new for now. Things used to be made with incredible detail and care</br>back then.</p>
		<p>Each season we start putting together images that we find inspiring and talk about what we want to bring to life in terms of function/design/color/materials. Often we'll be checking out leather suppliers and tradeshows at the same time and pull anything we have a gut reaction to.</p>
	</div>
	<div class="interview-section split-right">
		<img alt="" src="http://g.virbcdn.com/_assets/_graphics/_showcase/eayrslee-moneybag.jpg">
	</div>
	<div class="interview-section">
		<blockquote id="eayrslee-quote">“Everything kind of happens all at once, so there isn't</br>
		really a linear process, but in the end we edit and tie</br>
		it all together so that it makes sense for the brand.”</blockquote>
	</div>
	<div class="interview-section split-left">
		<h3>How has your experience been with Virb?</h3>
		<p>Virb enabled us to take control of the aesthetic of our site as well as manage and update content more often. Before we joined Virb, the site was often out of date because it wasn't updated very often. When customers go to the site, they want to see something new—otherwise, why check back? Having the ability to update the content easily enabled us to take charge of our site. Now we feel like big time programmers.</p>
		<blockquote>The interface is so easy to work with, and troubleshooting is <br />really easy.</blockquote>
		<p>We also love the Virb team. Everyone we've dealt with has been super friendly. We find ourselves telling people about Virb all the time.</p>
	</div>
